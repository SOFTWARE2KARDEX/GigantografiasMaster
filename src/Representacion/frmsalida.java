    private void btneliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btneliminarActionPerformed
        // TODO add your handling code here:
        if (!txtidsalida.getText().equals("")) {
            int confirmacion = JOptionPane.showConfirmDialog(rootPane, "¿Estás seguro de Eliminar La salida?","Confirmar",2);

            if (confirmacion==0) {
                fsalida func = new fsalida();
                vsalida dts= new vsalida();

                dts.setIdSalida(Integer.parseInt(txtidsalida.getText()));
                func.eliminar(dts);
                mostrar("");
                inhabilitar();

            }

        }
    }//GEN-LAST:event_btneliminarActionPerformed

