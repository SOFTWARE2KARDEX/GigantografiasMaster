    private void btnguardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnguardarActionPerformed
        // TODO add your handling code here:
     

        // Instanciando a los metodos sett y gett
        vsalida dts = new vsalida();
        //Instanciando a las funciones insertar, editar y eliminar
        fsalida func = new fsalida();

        int seleccionado = cbtiposalida.getSelectedIndex();
        dts.setTipoSalida((String) cbtiposalida.getItemAt(seleccionado));
        
        Calendar cal;
        int d,m,a;
        cal=dcfecha.getCalendar();
        d=cal.get(Calendar.DAY_OF_MONTH);
        m=cal.get(Calendar.MONTH);
        a=cal.get(Calendar.YEAR)-1900;             
        dts.setFecha(new Date(a,m,d));
        

        if (accion.equals("guardar")) {
            if (func.insertar(dts)) {
                JOptionPane.showMessageDialog(rootPane, "La salida fue registrado satisfactoriamente");
                mostrar("");
                inhabilitar();
            }

        }
        else if (accion.equals("editar")){
            dts.setIdSalida(Integer.parseInt(txtidsalida.getText()));

            if (func.editar(dts)) {
                JOptionPane.showMessageDialog(rootPane, "La Salida fue Editado satisfactoriamente");
                mostrar("");
                inhabilitar();
            }
        }
    }//GEN-LAST:event_btnguardarActionPerformed

