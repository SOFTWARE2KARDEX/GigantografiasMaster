    private void btneliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btneliminarActionPerformed
        // TODO add your handling code here:
        if (!txtiddetallesalida.getText().equals("")) {
            int confirmacion = JOptionPane.showConfirmDialog(rootPane, "¿Estás seguro de Eliminar?","Confirmar",2);

            if (confirmacion==0) {
                fdetallesalida func = new fdetallesalida();
                vdetallesalida dts= new vdetallesalida();

                dts.setIdDetalleSalida(Integer.parseInt(txtiddetallesalida.getText()));
                func.eliminar(dts);
                mostrar(idsalida);
                inhabilitar();

            }

        }
    }//GEN-LAST:event_btneliminarActionPerformed

