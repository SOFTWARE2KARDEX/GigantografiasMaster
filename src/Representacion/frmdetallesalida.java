    private void tablalistadoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablalistadoMouseClicked
        // TODO add your handling code here:
        btnguardar.setText("Editar");
        habilitar();
        jLabel2.setVisible(true);
        txtiddetallesalida.setVisible(true);
        btneliminar.setEnabled(true);
        accion="editar";

        int fila = tablalistado.rowAtPoint(evt.getPoint());

        txtiddetallesalida.setText(tablalistado.getValueAt(fila, 0).toString());
        txtidplancha.setText(tablalistado.getValueAt(fila, 1).toString());
        txtplancha.setText(tablalistado.getValueAt(fila, 2).toString());

        txtidsalida.setText(tablalistado.getValueAt(fila, 3).toString());
        txttipo.setText(tablalistado.getValueAt(fila, 4).toString());
        txtdescripcion.setText(tablalistado.getValueAt(fila, 5).toString());
        txtcantidad.setText(tablalistado.getValueAt(fila, 6).toString());
        txtidusuario.setText(tablalistado.getValueAt(fila, 7).toString());
        txtusuario.setText(tablalistado.getValueAt(fila, 8).toString());
        

    }//GEN-LAST:event_tablalistadoMouseClicked

